package Comment;
/**
 * Defines an enumeration of comment types that can be associated with comments.
 *
 * This class includes the following comment types:
 * - `SUGGESTION`: Represents a suggestion comment.
 * - `ENQUIRY`: Represents an enquiry comment.
 * - `BOTH`: Represents a comment that can be classified as both a suggestion and an enquiry.
 */
public class CommentType {
    /**
     * Enumeration of comment types.
     */
    public static enum COMMENTTYPES{SUGGESTION, ENQUIRY, BOTH}
}
