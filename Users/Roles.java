package Users;
/**
 * The `Roles` class defines an enumeration of possible roles for users.
 * These roles include STUDENT, STAFF, ATTENDEE, COMMITTEE, and STUDENTnSTAFF.
 */
public class Roles {
    /**
     * An enumeration of possible user roles.
     */
    public static enum ROLES {
        STUDENT, STAFF, ATTENDEE, COMMITTEE
    };
}
